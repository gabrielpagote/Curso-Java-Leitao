package fundamentos;

public class Inferencia {
	
	public static void main(String[] args) {
		
		double a = 4.5;
		System.out.println(a);
		
		//a linguagem irá determinar o tipo da variável com base no que escrevemos "var"
		//quando utilizamos var precisamos inicializar a variável logo em seguida, na mesma linha
		var b = 12.57;
		System.out.println(b);
		
		var c = "Texto";
		System.out.println(c);
		
		c = "Outro texto";
		System.out.println(c);
		
		double d; //declarando variavel
		d = 123.65; //inicializando variavvel
		
		
	}
}
